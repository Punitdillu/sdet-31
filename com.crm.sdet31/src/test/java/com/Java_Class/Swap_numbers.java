package com.Java_Class;

public class Swap_numbers {

		public static void main(String[] args) {
			{
				int a=2;
				int y=3;
				
				a=a+y;
				y=a-y;
				a=a-y;
				System.out.println(a);
				System.out.println(y);
				
				int c=3;
				int d=7;
				int temp;
				temp=c;
				c=d;
				d=temp;
				System.out.println(c);
				System.out.println(d);
			}
		}
}
